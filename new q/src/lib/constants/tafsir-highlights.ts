function parseTerms(raw: string) {
  return raw
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean);
}

function uniqueTerms(...groups: string[][]) {
  return Array.from(new Set(groups.flat())).sort((left, right) => right.length - left.length);
}

